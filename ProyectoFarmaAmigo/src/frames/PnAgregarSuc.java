    public void llenadoFarm(JComboBox cb)
    {
        ArrayList<String> f = ManipulaSelecciones.select("SELECT * FROM farmacia f;");
        for (int i = 0; i < f.size(); i++)
        {
            cb.addItem(f.get(i));
        }
    }


