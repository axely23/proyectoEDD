    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAgregarActionPerformed
    {//GEN-HEADEREND:event_btnAgregarActionPerformed
        if (VtnPrincipal.vComponentes(cClaveS, cNombreS, cEstado, cCiudad, cTelefono))
        {
            msj.setVisible(true);
            msj.setText("<html>" + VtnPrincipal.componentes(cClaveS, cNombreS, cEstado, cCiudad, cTelefono) + "<html>");
        } else
        {
            if (VtnPrincipal.validaCad(cNombreS.getText(), "^(?!\\s)(.*\\S)?(?!\\s)$"))
            {

                if (ManipulaSelecciones.validaCampo("SELECT s.nombreSuc FROM  sucursal s;", cNombreS.getText()))
                {
                    if (ManipulaSelecciones.validaCampo("SELECT s.claveSuc FROM  sucursal s;", cClaveS.getText()))
                    {
                        Crud.insercion(
                                "INSERT INTO sucursal (nombreSuc, nombreFarm, claveSuc, estado, ciudad, telefono) VALUES ("
                                + "'" + cNombreS.getText() + "'" + ", "
                                + "'" + String.valueOf(cmFarmaciaSuc.getSelectedItem()) + "'" + ", "
                                + "'" + cClaveS.getText() + "'" + ", "
                                + "'" + cEstado.getText() + "'" + ", "
                                + "'" + cCiudad.getText() + "'" + ", "
                                + cTelefono.getText()
                                + "); "
                        );
                        msj.setVisible(false);
                        CtrlInterfaz.limpia(cClaveS, cNombreS, cEstado, cCiudad, cTelefono);
                        JOptionPane.showMessageDialog(null, "Sucursal agregada con exito", "Altas", JOptionPane.PLAIN_MESSAGE, icono("/imagenes/comprobado.png", 50, 50));
                    } else
                    {
                        msj.setVisible(false);
                        JOptionPane.showMessageDialog(null, "Clave de sucursal ya existente", "Altas", JOptionPane.PLAIN_MESSAGE, icono("/imagenes/error.png", 50, 50));
                        CtrlInterfaz.selecciona(cClaveS);
                    }

                } else
                {
                    msj.setVisible(false);
                    JOptionPane.showMessageDialog(null, "Nombre de sucursal ya existente", "Altas", JOptionPane.PLAIN_MESSAGE, icono("/imagenes/error.png", 50, 50));
                    CtrlInterfaz.selecciona(cNombreS);
                }

            } else
            {
                msj.setVisible(true);
                msj.setText("<html>" + "Nombre de sucursal no valido" + "<html>");
                CtrlInterfaz.selecciona(cNombreS);
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

